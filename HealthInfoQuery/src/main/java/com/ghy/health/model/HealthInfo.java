package com.ghy.health.model;

/**
 * @author JY
 * 体测的全部信息
 */
public class HealthInfo {
	private int year;
	private int height;
	private int weight;
	private int running800;
	
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public int getWeight() {
		return weight;
	}
	public void setWeight(int weight) {
		this.weight = weight;
	}
	public int getRunning800() {
		return running800;
	}
	public void setRunning800(int running800) {
		this.running800 = running800;
	}
	
}
